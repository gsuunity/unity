    private void nicknKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_nicknKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode() == KeyEvent.VK_ENTER) {
            try { 
            String query = "Select * from log2 where gsuemail=? and nickname=? ";
            pst = con.prepareStatement(query);
            pst.setString(1, emailaddress.getText());
            pst.setString(2, nickn.getText());
            rs = pst.executeQuery();
            int count = 0;
            String pass = "";
            
            while(rs.next()){
                count = count +1;
                pass = rs.getString("pw");
            }
            if(count == 1) {   
                JOptionPane.showMessageDialog(null,  "your password is " + pass);
                this.dispose();              
            }
            
            else {
                JOptionPane.showMessageDialog(null, "email or nickname is not correct. Please try again");
            }
            
            pst.close();
            rs.close();
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
            
        }
        
    }//GEN-LAST:event_nicknKeyPressed

